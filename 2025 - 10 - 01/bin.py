# Recapitulando
intValor_1 = 195
intValor_2 = 89

print(f'Valor 1................: {intValor_1:>3} -> {intValor_1:08b}')
print(f'Valor 2................: {intValor_2:>3} -> {intValor_2:08b}')

print(f'intValor_1 & intValor_2: {intValor_1 & intValor_2:>3} -> {(intValor_1 & intValor_2):08b}')
print(f'intValor_1 | intValor_2: {intValor_1 | intValor_2:>3} -> {(intValor_1 | intValor_2):08b}')
print(f'intValor_1 ^ intValor_2: {intValor_1 ^ intValor_2:>3} -> {(intValor_1 ^ intValor_2):08b}')